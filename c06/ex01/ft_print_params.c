#include<unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	while (i < argc)
	{
		while (*argv[j])
		{
			write(1, argv[j], 1);
			argv[j]++;
		}
		write(1, "\n", 1);
		i++;
		j++;
	}
}
