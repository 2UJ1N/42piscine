int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	if (str == 0)
		return (1);
	while (str[i])
	{
		if ('a' <= str[i] && str[i] <= 'z')
			i ++;
		else
			return (0);
	}
	return (1);
}
