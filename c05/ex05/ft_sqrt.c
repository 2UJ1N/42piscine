int	ft_sqrt(int nb)
{
	long long	i;

	i = 1;
	if (nb == 1)
		return (1);
	while (i * i <= (long long)nb)
	{
		if (i * i == (long long)nb)
			return (i);
		i++;
	}
	return (0);
}
